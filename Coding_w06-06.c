#include <stdio.h> //  สำหรับฟังก์ชัน printf

int main() { // จุดเริ่มต้นของโปรแกรม

    // สร้างตัวแปรชนิดจำนวนเต็ม และกำหนดค่าเริ่มต้นให้
    int a = 5, b = 2;

    // สร้างตัวแปรชนิดทศนิยม (เลขมีจุด) และกำหนดค่าให้
    float x = 3.0, y = 4.5;

    // คำนวณค่า r1:
    // a++ คือใช้ค่า a ก่อน (คือ 5) แล้วค่อยเพิ่มทีหลังเป็น 6
    // (5 * 2) = 10
    // แปลง y (4.5) เป็นเลขจำนวนเต็มได้ 4 แล้วหาเศษจากการหารด้วย 3 ได้ 1
    // รวมกันคือ 10 + 1 = 11
    int r1 = a++ * b + (int)y % 3;
    printf("r1 = %d\n", r1); // แสดงผล: r1 = 11

    // คำนวณค่า r2:
    // ตอนนี้ a เป็น 6 แล้ว เพราะถูกเพิ่มมาเมื่อกี้
    // เช็คว่า a มากกว่า b หรือไม่ -> (6 > 2) จริง
    // เอา x มาแปลงเป็น int ได้ 3 แล้วหารด้วย b (3 / 2 = 1) ซึ่งน้อยกว่า 2 -> จริง
    // จริง && จริง = จริง -> ค่าคือ 1
    int r2 = (a > b) && ((int)x / b < 2);
    printf("r2 = %d\n", r2); // แสดงผล: r2 = 1

    // คำนวณค่า r3:
    // ++x คือ เพิ่มค่า x ก่อน จาก 3.0 เป็น 4.0 แล้วค่อยเอาไปใช้
    // 4.0 * 4.5 = 18.0
    // a ตอนนี้เป็น 6 แล้ว -> 6 / 2 = 3 
    // 18.0 - 3 = 15.0
    float r3 = (++x * y - a / 2);
    printf("r3 = %.2f\n", r3); // แสดงผล: r3 = 15.00

    // คำนวณค่า r4:
    // เอา x (ตอนนี้เป็น 4.0) ไปบวกเพิ่ม 1.5 ได้เป็น 5.5
    // เช็คว่า 5.5 มากกว่า y (4.5) หรือเปล่า -> จริง
    // || คือ "หรือ" ถ้าเงื่อนไขหน้าเป็นจริง ก็ไม่ต้องดูข้างหลังเลย
    // ข้างหลังคือ b-- > 0 -> ไม่ถูกประเมิน เพราะด้านหน้าจริงแล้ว
    // จริง || อะไรก็แล้วแต่ = จริง -> ค่าคือ 1.0
    float r4 = ((x += 1.5) > y) || (b-- > 0);
    printf("r4 = %.2f\n", r4); // แสดงผล: r4 = 1.00

    return 0; // จบโปรแกรม
}
