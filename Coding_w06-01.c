#include <stdio.h> // สำหรับฟังก์ชัน printf


int main() {
    // กำหนดค่าตัวแปรเริ่มต้น
    int a = 5;       // ตัวแปร a เก็บเลขจำนวนเต็ม เริ่มจาก 5
    int b = 17;      // ตัวแปร b ก็เป็นจำนวนเต็ม เก็บค่า 17
    double c = 8.5;  // ตัวแปร c เก็บเลขทศนิยม 8.5
    double d = 4.0;  // ตัวแปร d เก็บเลขทศนิยม 4.0

    // แสดงค่าตัวแปรทั้งหมด
    printf("บรรทัดที่ 1: ค่าเริ่มต้นของตัวแปร:\n");
    printf("   a = %d\n", a);
    printf("   b = %d\n", b);
    printf("   c = %.1f\n", c);
    printf("   d = %.1f\n", d);
    printf("------------------------------\n");

    // บวกเลข: d + a
    double result_d_plus_a = d + a; // เอา d บวกกับ a แล้วบันทึกผลลัพธ์ไว้
    printf("บรรทัดที่ 2: d + a = %.1f\n", result_d_plus_a);
    printf("------------------------------\n");

    // ลบเลข: a - b
    int result_a_minus_b = a - b; // ลบค่า b ออกจาก a
    printf("บรรทัดที่ 3: a - b = %d\n", result_a_minus_b);
    printf("------------------------------\n");

    // คูณเลขทศนิยม: c * d
    double result_c_times_d = c * d;
    printf("บรรทัดที่ 4: c * d = %.1f\n", result_c_times_d);
    printf("------------------------------\n");

    // คูณเลขจำนวนเต็มกับทศนิยม: a * c
    double result_a_times_c = a * c;
    printf("บรรทัดที่ 5: a * c = %.1f\n", result_a_times_c);
    printf("------------------------------\n");

    // หารเลขทศนิยม: c / d
    double result_c_div_d = c / d;
    printf("บรรทัดที่ 6: c / d = %.3f\n", result_c_div_d);
    printf("------------------------------\n");

    // แปลง b เป็นทศนิยมก่อนหาร: b / c
    double result_b_div_c = (double)b / c;
    printf("บรรทัดที่ 7: b / c = %.3f\n", result_b_div_c);
    printf("------------------------------\n");

    // หารเอาเศษ: a % b (ใช้ได้เฉพาะเลขเต็ม)
    int result_a_mod_b = a % b;
    printf("บรรทัดที่ 8: a %% b = %d\n", result_a_mod_b);
    printf("------------------------------\n");

    // หารเอาเศษของเลขทศนิยม: ทำไม่ได้โดยตรง ต้องใช้ fmod() แทน
    printf("บรรทัดที่ 9: ใช้ %% ไม่ได้ เพราะ c เป็นเลขทศนิยม\n");
    printf("------------------------------\n");

    printf("บรรทัดที่ 10: ใช้ %% ไม่ได้ เพราะทั้ง c และ d เป็นเลขทศนิยม\n");
    printf("------------------------------\n");

    return 0; // จบโปรแกรม
}
